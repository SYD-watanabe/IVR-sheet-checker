#!/bin/bash
# Git Bash用 - シンプルコマンド集
# コピー&ペーストで実行できます

# ========================================
# 基本的なプッシュ手順（最小限）
# ========================================

# 1. プロジェクトフォルダに移動（パスを実際の場所に変更）
cd /c/Users/YourName/Projects/IVR-sheet-checker

# 2. 状態確認
git status

# 3. すべての変更を追加
git add .

# 4. コミット（シンプル版）
git commit -m "feat: バッチ処理改善と修正済み文章コピー機能追加 (v1.1.0)"

# 5. プッシュ
git push


# ========================================
# 詳細なコミットメッセージ版（推奨）
# ========================================

# 1-3は上と同じ

# 4. コミット（詳細版）
git commit -m "feat: バッチ処理改善と修正済み文章コピー機能追加" \
-m "" \
-m "主な変更:" \
-m "- バッチサイズを10に増量（RPM削減: 1/10）" \
-m "- バッチ間に3秒待機（レート制限対策強化）" \
-m "- 修正済み文章の自動生成機能を追加" \
-m "- ワンクリックコピー機能を実装" \
-m "" \
-m "Version: 1.1.0"

# 5. プッシュ
git push


# ========================================
# 初めてプッシュする場合
# ========================================

# リポジトリの初期化（まだしていない場合）
git init

# リモートリポジトリを追加
git remote add origin https://github.com/YOUR_USERNAME/IVR-sheet-checker.git

# ブランチ名をmainに設定
git branch -M main

# 全ファイルを追加
git add .

# 初回コミット
git commit -m "feat: バッチ処理改善と修正済み文章コピー機能追加 (v1.1.0)"

# 初回プッシュ
git push -u origin main


# ========================================
# よく使うコマンド
# ========================================

# 現在の状態を確認
git status

# コミット履歴を確認
git log --oneline -5

# リモートリポジトリを確認
git remote -v

# ブランチを確認
git branch

# 最新の変更を取得
git pull

# 変更の差分を確認
git diff

# 最後のコミットを確認
git show
